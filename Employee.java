package java8;

public class Employee {

	public int id;
	public String name;
	public String email;
	public double salary;
	public String address;
	
	public Employee(int id, String name, String email, double salary, String address) {
		this.id = id;
		this.name = name;
		this.email = email;
		this.salary = salary;
		this.address = address;
	}
}